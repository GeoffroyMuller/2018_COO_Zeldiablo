package zeldiablo;

public class Coordonnee {
	private int x,y;
	
	public Coordonnee(int cX, int cY) {
		this.x = cX;
		this.y = cY;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
}
